      SUBROUTINE BMG2_SymStd_SETUP_PtrLS( &
     &                       pLS, pSI_MSG, NProcI, NProcJ, NOG&
     &                       )

! ======================================================================
!  --------------------
!   DESCRIPTION:
!  --------------------
!
!     Setup pointers into the line-solve workspace.
!
! ======================================================================
! $license_flag$
! ======================================================================
!  --------------------
!   INPUT:
!  --------------------
!
!
! ======================================================================
!  --------------------
!   INPUT/OUTPUT:
!  --------------------
!
!
! ======================================================================
!  --------------------
!   OUTPUT:
!  --------------------
!
!
! ======================================================================
!  --------------------
!   LOCAL:
!  --------------------
!
!
! ======================================================================

      IMPLICIT   NONE

! -----------------------------
!     Includes
!
      INCLUDE    'BMG_workspace_f90.h'
      INCLUDE    'BMG_constants_f90.h'

! ---------------------------
!     Argument Declarations:
!
      INTEGER  NOG, NProcI, NProcJ, pSI_MSG
      INTEGER  pLS(NBMG_pLS,NOG)

! ----------------------------
!     Local Declarations:
!
      INTEGER kg, ptr

! ======================================================================

      ptr = pSI_MSG

      DO kg=NOG, 1, -1

         pLS(ipL_LS_XDataDist,kg) = ptr
         ptr = ptr+2*NProcI

         pLS(ipL_LS_YDataDist,kg) = ptr
         ptr = ptr+2*NProcJ

      ENDDO

      pSI_MSG = ptr

! ======================================================================

      RETURN
      END
